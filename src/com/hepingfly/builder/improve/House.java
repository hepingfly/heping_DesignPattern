package com.hepingfly.builder.improve;

/**
 * 产品-->product
 */
public class House {
    private String basie;
    private String wall;
    private String roofed;

    public String getBasie() {
        return basie;
    }

    public void setBasie(String basie) {
        this.basie = basie;
    }

    public String getWall() {
        return wall;
    }

    public void setWall(String wall) {
        this.wall = wall;
    }

    public String getRoofed() {
        return roofed;
    }

    public void setRoofed(String roofed) {
        this.roofed = roofed;
    }
}
